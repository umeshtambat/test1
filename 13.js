function applyColor(p1) {
  let h1ref = document.querySelector("h1");
  h1ref.style.color = p1;
}

function resetColor() {
  let h1ref = document.querySelector("h1");
  h1ref.style.color = "black";
}

function applyFont(p1) {
  let pref = document.querySelector("#pid1");
  pref.style.fontSize = p1;
}
